// 7. Wap To swap two number


class SwapTwoNoP7
{
	public static void main(String args[])
	{
		int a=10,b=20,t;
		
		System.out.println("Before Swapping is A = "+a);
		System.out.println("Before Swapping is B = "+b);

		t=a;
		a=b;
		b=t;

		System.out.println("After Swapping is A = "+a);
		System.out.println("After Swapping is B = "+b);	
	}
}